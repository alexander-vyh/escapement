def _load_manifest(path: Path) -> dict[str, Any]:
    return json.loads(path.read_text(encoding="utf-8"))


def _read_fragment(root: Path, rel: str) -> str:
    path = root / rel
    return path.read_text(encoding="utf-8").strip()


def _render_document(root: Path, manifest: dict[str, Any], host: str) -> str:
    docs = manifest["documents"]
    host_doc = docs["hosts"][host]
    parts = [GENERATED_HEADER, "", f"# {host_doc['title']}", ""]
    for rel in docs["shared_fragments"]:
        parts.append(_read_fragment(root, rel))
        parts.append("")
    for rel in host_doc["fragments"]:
        parts.append(_read_fragment(root, rel))
        parts.append("")
    return "\n".join(parts).rstrip() + "\n"


def _render_codex_hooks(manifest: dict[str, Any]) -> str:
    hooks: dict[str, list[dict[str, Any]]] = {}
    for hook in manifest.get("hooks", []):
        host = hook.get("hosts", {}).get("codex", {})
        if host.get("status") != "ready":
            continue
        for event in host.get("events", []):
            command = event["command"]
            item: dict[str, Any] = {
                "matcher": event.get("matcher", ""),
                "hooks": [{"type": "command", "command": command}],
            }
            timeout = event.get("timeout_seconds")
            if timeout is not None:
                item["hooks"][0]["timeout"] = timeout
            hooks.setdefault(event["event"], []).append(item)
    return json.dumps({"hooks": hooks}, indent=2, sort_keys=True) + "\n"


